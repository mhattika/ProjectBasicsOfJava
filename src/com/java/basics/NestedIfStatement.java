package com.java.basics;

public class NestedIfStatement 
{

	public static void main(String[] args) 
	{
	int	dayCode=7;
	
	if(dayCode==1) {System.out.println("Sunday");
	}
	else if (dayCode==2) {System.out.println("Monday");}
	else if (dayCode==3) {System.out.println("Tuesday");}
	else if (dayCode==4) {System.out.println("Wednessday");}
	else if (dayCode==5) {System.out.println("Thursday");}
	else if (dayCode==6) {System.out.println("Friday");}
	
	else {System.out.println("Saturday");}
		
	}

}
