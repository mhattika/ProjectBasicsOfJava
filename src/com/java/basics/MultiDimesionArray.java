package com.java.basics;

public class MultiDimesionArray {

	public static void main(String[] args) {
		int a[]= new int[3]; //fixed array
		a[0]=10;
		a[1]=20;
		a[2]=30;
		
		int m[][]= new int[3][2];//fixed array
		m[0][0]=0; m[0][1]=00;
		m[1][0]=10; m[1][1]=20;
		m[2][0]=20; m[2][1]=20;


	}

}
